    private void lstDepartamentosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstDepartamentosValueChanged
        try {
            db.comando = (Statement) db.conexion.createStatement();
            String query="SELECT * FROM Departamento WHERE Nombre='"+lstDepartamentos.getSelectedValue()+"'";
            db.registro = db.comando.executeQuery(query);
            DefaultListModel modelo=new DefaultListModel();
            while (db.registro.next()) {
                txtNo.setText(db.registro.getString(1));
                txtNombre.setText(db.registro.getString(2));
                txtDescripcion.setText(db.registro.getString(3));
            }
            txtNombre.setEnabled(true);
            txtDescripcion.setEnabled(true);
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        } catch (SQLException ex) {
            Logger.getLogger(Departamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lstDepartamentosValueChanged

