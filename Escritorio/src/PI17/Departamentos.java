    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int dialog=JOptionPane.showConfirmDialog(null, "ADVERTENCIA: TODOS LOS TICKETS RELACIONADOS CON ESTA AREA NO TENDRAN UN AREA ESPECIFICA\nPARA REALIZAR ESTA ACCION, NO DEBE DE HABER ESPECIALISTAS CON ESTA AREA\nÂ¿Seguro que desea continuar?");
        if (dialog==JOptionPane.YES_OPTION) {
            try {
                db.comando = (Statement) db.conexion.createStatement();
                String query="DELETE FROM Departamento WHERE Id_Departamento="+txtNo.getText()+"";
                System.out.println(query);
                PreparedStatement preparedStmt = con.prepareStatement(query);
                preparedStmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Se ha borrado el departamento seleccionado");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido borrar el departamento, verifique que no haya especialistas en este dpto.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre="", descripcion="";
        nombre=JOptionPane.showInputDialog("Nombre del departamento");
        if (nombre!=null) {
            descripcion=JOptionPane.showInputDialog("Descripcion del departamento "+nombre);
            if (descripcion!=null) {
                try {
                    db.comando = (Statement) db.conexion.createStatement();
                    String query="INSERT INTO Departamento (Id_Departamento, Nombre, Descripcion) VALUES (NULL, '"+nombre+"','"+descripcion+"')";
                    System.out.println(query);
                    PreparedStatement preparedStmt = con.prepareStatement(query);
                    preparedStmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Se ha agregado el departamento ");
                    mostrarDepartamentos();
                } catch (SQLException ex) {
                    Logger.getLogger(Departamentos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

