    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String escuela="";
        escuela=JOptionPane.showInputDialog(null, "Nombre de la escuela", "Escuela", 1);
        System.out.println(escuela);
        if (escuela != null) {
            try {
                db.comando = (Statement) db.conexion.createStatement();
                String query="UPDATE Escuelas SET Nombre='"+escuela.toUpperCase()+"' WHERE Nombre='"+lstEscuelas.getSelectedValue()+"'";
                PreparedStatement preparedStmt = con.prepareStatement(query);
                preparedStmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "La escuela fue actualizada");
                mostrarEscuelas();
            } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(null, "La escuela no pudo ser actualizada");
                 
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int dialog=JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar esta escuela?");
        if (dialog==JOptionPane.YES_OPTION) {
            try {
                db.comando = (Statement) db.conexion.createStatement();
                String query="DELETE FROM Escuelas WHERE Nombre='"+lstEscuelas.getSelectedValue()+"'";
                System.out.println(query);
                PreparedStatement preparedStmt = con.prepareStatement(query);
                preparedStmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Se ha borrado la escuela seleccionada");
                mostrarEscuelas();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido borrar la escuela seleccionada");
                System.out.println(ex.toString());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nuevo="";
        nuevo=JOptionPane.showInputDialog(null, "Nombre de la nueva escuela", "Agregar escuela", 1);
        if (nuevo!=null) {
            try {
                db.comando = (Statement) db.conexion.createStatement();
                String query="INSERT INTO Escuelas (Id_Escuela, Nombre) VALUES (NULL, '"+nuevo.toUpperCase()+"')";
                System.out.println(query);
                PreparedStatement preparedStmt = con.prepareStatement(query);
                preparedStmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Se ha agregado la escuela ");
                mostrarEscuelas();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido agregar la escuela ");
                System.out.println(ex.toString());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

