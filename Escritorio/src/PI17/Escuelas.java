    private void mostrarEscuelas(){
        try {
            
            db.comando = (Statement) db.conexion.createStatement();
            String query="SELECT * FROM Escuelas ORDER BY 2 ASC";
            db.registro = db.comando.executeQuery(query); 
            DefaultListModel modelo=new DefaultListModel();
            while (db.registro.next()) {
                modelo.addElement(db.registro.getString(2));
            }
            lstEscuelas.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Escuelas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

